package com.kingtone.jw.service.jwoa.bean;

/**
 * ProcInfo当前处理过程列表
 * @author     litengmin
 * @since      [产品/模块版本] （必须）
 * @date 2013-8-6
 */

public class ProcInfo {
    private String nodeName;//NODENAME="签收" 处理步骤
    private String userList;//USERLIST="詹红,指挥中心收文员" 处理人列表
    private String timeLimit;//TIMELIMIT="--(h)" 时限要求（用时） 
    private String outTime;//OUTTIME="--" 延误时间
    private String isOpen;//ISOPEN="已阅"   任务是否已阅
    private String nodeId;//NODEID="2971" 流程节点编号 
    private String recvTime;//RECVTIME="2013-7-19 11:57:03" 到达时间
    private String procTime;//PROCTIME="2013-7-19 11:57:03" 处理时间
    private String type;//TYPE="0" 记录类型（1：历史记录；0： 当前处理记录）
    private String procId;//PROCID="22336815"流程ID
    private String procName;//PROCNAME    流程节点名称
    private String note;//NOTE    处理意见
    private String orgList;//ORGLIST     处理部门列表

    
    public String getProcName() {
        return procName;
    }
    public void setProcName(String procName) {
        this.procName = procName;
    }
    public String getNote() {
        return note;
    }
    public void setNote(String note) {
        this.note = note;
    }
    public String getOrgList() {
        return orgList;
    }
    public void setOrgList(String orgList) {
        this.orgList = orgList;
    }
    public String getNodeName() {
        return nodeName;
    }
    public void setNodeName(String nodeName) {
        this.nodeName = nodeName;
    }
    public String getUserList() {
        return userList;
    }
    public void setUserList(String userList) {
        this.userList = userList;
    }
    public String getTimeLimit() {
        return timeLimit;
    }
    public void setTimeLimit(String timeLimit) {
        this.timeLimit = timeLimit;
    }
    public String getOutTime() {
        return outTime;
    }
    public void setOutTime(String outTime) {
        this.outTime = outTime;
    }
    public String getIsOpen() {
        return isOpen;
    }
    public void setIsOpen(String isOpen) {
        this.isOpen = isOpen;
    }
    public String getNodeId() {
        return nodeId;
    }
    public void setNodeId(String nodeId) {
        this.nodeId = nodeId;
    }
    public String getRecvTime() {
        return recvTime;
    }
    public void setRecvTime(String recvTime) {
        this.recvTime = recvTime;
    }
    public String getProcTime() {
        return procTime;
    }
    public void setProcTime(String procTime) {
        this.procTime = procTime;
    }
    public String getType() {
        return type;
    }
    public void setType(String type) {
        this.type = type;
    }
    public String getProcId() {
        return procId;
    }
    public void setProcId(String procId) {
        this.procId = procId;
    }
}
