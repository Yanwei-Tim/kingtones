package com.kingtone.jw.biz.common.domain;

import java.util.Date;



public class JwUpdate {
	private String id; //主键
	private String name;//警种名称
	private String  description;
	private Date time;
	private String name_copy;
	private String version;
	private   String state;
	
	
	public String getVersion() {
		return version;
	}
	public void setVersion(String version) {
		this.version = version;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getName_copy() {
		return name_copy;
	}
	public void setName_copy(String name_copy) {
		this.name_copy = name_copy;
	}
	public Date getTime() {
		return time;
	}
	public void setTime(Date time) {
		this.time = time;
	}
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
	
	
	
}
