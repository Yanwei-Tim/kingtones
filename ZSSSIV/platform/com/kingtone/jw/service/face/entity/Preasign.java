package com.kingtone.jw.service.face.entity;

import javax.xml.bind.annotation.XmlAccessType;
import javax.xml.bind.annotation.XmlAccessorType;
import javax.xml.bind.annotation.XmlAttribute;
import javax.xml.bind.annotation.XmlElement;
import javax.xml.bind.annotation.XmlRootElement;

/**
 * 返回查询结果的具体明细内容，由返回结果的根实体调用
 * @author yangzm
 *
 */
@XmlRootElement
@XmlAccessorType(XmlAccessType.FIELD)
public class Preasign {

	@XmlAttribute(name = "id")
	private String id;

	@XmlElement(name = "iPM")
	private int sort;

	@XmlElement(name = "CaseID")
	private String caseID;

	@XmlElement(name = "XM")
	private String name;

	@XmlElement(name = "SFZHM")
	private String idNumber;

	@XmlElement(name = "Img")
	private String img;

	@XmlElement(name = "fScore")
	private String score;

	@XmlElement(name = "BusinessSysNum")
	private String businessSysNum;

	@XmlElement(name = "BusinessSysKey")
	private String businessSysKey;

	@XmlElement(name = "KLB")
	private String libSubject;

	@XmlElement(name = "DataBase")
	private String dataBase;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public int getSort() {
		return sort;
	}

	public void setSort(int sort) {
		this.sort = sort;
	}

	public String getCaseID() {
		return caseID;
	}

	public void setCaseID(String caseID) {
		this.caseID = caseID;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getIdNumber() {
		return idNumber;
	}

	public void setIdNumber(String idNumber) {
		this.idNumber = idNumber;
	}

	public String getImg() {
		return img;
	}

	public void setImg(String img) {
		this.img = img;
	}

	public String getScore() {
		return score;
	}

	public void setScore(String score) {
		this.score = score;
	}

	public String getBusinessSysNum() {
		return businessSysNum;
	}

	public void setBusinessSysNum(String businessSysNum) {
		this.businessSysNum = businessSysNum;
	}

	public String getBusinessSysKey() {
		return businessSysKey;
	}

	public void setBusinessSysKey(String businessSysKey) {
		this.businessSysKey = businessSysKey;
	}

	public String getLibSubject() {
		return libSubject;
	}

	public void setLibSubject(String libSubject) {
		this.libSubject = libSubject;
	}

	public String getDataBase() {
		return dataBase;
	}

	public void setDataBase(String dataBase) {
		this.dataBase = dataBase;
	}

}
