package com.kingtone.jw.biz.xajw.domain;

import java.util.Date;

/**
 * GPS数据基类，关联表jw_gps_date
 * 
 * @author hanliqiang@kingtoneinfo.com
 * 
 */

public class JwGpsData {
	private String userid;// 用户ID
	private String speed;// 速度
	private String direction;// 方向
	private String imsi;// imsi码
	private String deviceid;// 设备串号
	private String x;// 维度
	private String y;// 经度
	private Date datetime;// 时间
	private String userName;//用户名称
	private String startdate; // 起始时间 （用于查询条件）
	private String enddate; // 结束时间 （用于查询条件）
	private String keyValue; // 联合组建
	
	private String phone;//手机号码
	private String account;//账号
	private int uaid;//名 账 判断
	
	public int getUaid() {
		return uaid;
	}

	public void setUaid(int uaid) {
		this.uaid = uaid;
	}

	public String getAccount() {
		return account;
	}

	public void setAccount(String account) {
		this.account = account;
	}

	public String getPhone() {
		return phone;
	}

	public void setPhone(String phone) {
		this.phone = phone;
	}

	/**
	 * @return Returns the keyValue.
	 */
	public String getKeyValue() {
		return keyValue;
	}

	/**
	 * @param keyValue The keyValue to set.
	 */
	public void setKeyValue(String keyValue) {
		this.keyValue = keyValue;
	}

	/**
	 * @return Returns the userid.
	 */
	public String getUserid() {
		return userid;
	}

	/**
	 * @param userid The userid to set.
	 */
	public void setUserid(String userid) {
		this.userid = userid;
	}

	/**
	 * @return Returns the deviceid.
	 */
	public String getDeviceid() {
		return deviceid;
	}

	/**
	 * @param deviceid The deviceid to set.
	 */
	public void setDeviceid(String deviceid) {
		this.deviceid = deviceid;
	}

	/**
	 * @return Returns the datetime.
	 */
	public Date getDatetime() {
		return datetime;
	}

	/**
	 * @param datetime The datetime to set.
	 */
	public void setDatetime(Date datetime) {
		this.datetime = datetime;
	}

	/**
	 * @return Returns the 起始时间.
	 */
	public String getStartdate() {
		return startdate;
	}

	/**
	 * @param startdate The 起始时间 to set.
	 */
	public void setStartdate(String startdate) {
		this.startdate = startdate;
	}

	/**
	 * @return Returns the 结束时间.
	 */
	public String getEnddate() {
		return enddate;
	}

	/**
	 * @param enddate The 结束时间 to set.
	 */
	public void setEnddate(String enddate) {
		this.enddate = enddate;
	}

	public String getSpeed() {
		return speed;
	}

	public void setSpeed(String speed) {
		this.speed = speed;
	}

	public String getDirection() {
		return direction;
	}

	public void setDirection(String direction) {
		this.direction = direction;
	}

	public String getImsi() {
		return imsi;
	}

	public void setImsi(String imsi) {
		this.imsi = imsi;
	}

	public String getX() {
		return x;
	}

	public void setX(String x) {
		this.x = x;
	}

	public String getY() {
		return y;
	}

	public void setY(String y) {
		this.y = y;
	}

	public String getUserName() {
		return userName;
	}

	public void setUserName(String userName) {
		this.userName = userName;
	}

}
