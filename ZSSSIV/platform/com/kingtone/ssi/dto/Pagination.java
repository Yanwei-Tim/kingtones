/**
 * <p>[描述信息：说明类的基本功能]</p>
 *
 * @author 邹甲乐 - zoujiale@kingtoneinfo.com
 * @version 1.0 Created on Aug 20, 2008 2:04:41 PM
 */
public class Pagination {

	private int start;

	private int limit;

	private int totalCount;

	/**
	 *
	 * @return 
	 */
	public int getTotalCount() {
		return totalCount;
	}

	/**
	 *
	 * @param totalCount
	 */
	public void setTotalCount(int totalCount) {
		this.totalCount = totalCount;
	}

	/**
	 *
	 * @return 
	 */
	public int getStart() {
		return start;
	}

	/**
	 *
	 * @param start
	 */
	public void setStart(int start) {
		this.start = start;
	}

	/**
	 *
	 * @return 
	 */
	public int getLimit() {
		return limit;
	}

	/**
	 *
	 * @param limit
	 */
	public void setLimit(int limit) {
		this.limit = limit;
	}
}
